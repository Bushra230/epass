        class="pl-2">আপনার অধীনে ন্যূনতম তিন মাসকাল চাকুরী করিয়াছেন এমন প্রত্যেক অফিসার/কর্মচারীর প্রতিবেদন লিখিবেন।</span>
</p>

<h6 class="pt-4 pb-2 mb-0 "><b>প্রতিস্বাক্ষরকারী অফিসারের জন্য নির্দেশঃ</b></h6>
<p class="pl-4 d-flex ">
