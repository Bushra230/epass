<h6 class="pt-3 pb-2 mb-2 "><b>ফরম পূরণের নির্দেশাবলীঃ</b></h6>
<p class="pl-4 d-flex">
    <span class="w-26 d-inline-block">১।</span>
    <span class="pl-2 text-justify"><span class="pr-1">(ক)</span>
        নন-গেজেটেড অফিসার/কর্মচারীদের কিছু মৌলিক গুণ ও কার্য-সম্পাদনের মান প্রতিবৎসরান্তে লিপিবদ্ধ করার
    জন্য এই ফরমটি তৈরী করা হইয়াছে। বিশেষজ্ঞ পর্যায়ের চাকুরী অথবা বিশেষ ধরণের কর্তব্যে নিযুক্ত অফিসার/কর্মচারীদের জন্য
    অন্যান্য প্রয়ােজনীয় বিশেষ গুণ সম্পর্কে প্রয়ােজনবােধে ১ম পাতার ১৬ হইতে ১৯ ক্রমিকের শূন্য স্থানে মন্তব্য লিপিবদ্ধ
    করিতে হইবে, মন্ত্রণালয়/বিভাগ/পরিদপ্তরসমূহ প্রতিস্বাক্ষরকারী/প্রতিবেদনকারী অফিসারকে এই সম্পর্কে প্রশাসনিক নির্দেশ
    প্রদান করিবেন এবং বিশেষ গুণাবলীর উপযুক্ত শিরােনামসমূহ উক্ত কলামে টাইপ করিতে হইলে। যদি কোন সংস্থা/অফিস ৪টি শূন্য
    ক্রমিকের অতিরিক্ত কোন দফা সংযােজন আবশ্যক বলিয়া মনে করেন, তাহা হইলে অতিরিক্ত পৃষ্ঠা যােগ করিয়া উহা করিতে পারেন। যদি
    কোন সংস্থা/অফিস কোন বিশেষ শ্রেণীর অফিসার/কর্মচারীদের ক্ষেত্রেই ফরমের কোন কলাম অপূর্ণ রাখিতে চান, তাহা হইলে মন্তব্যের
    ঘরে “প্রযােজ্য নহে” লিখিতে হইবে। এইমর্মে প্রতিবেদনকারী অফিসারদিগকে প্রয়ােজনীয় নির্দেশ প্রদান করিয়া উহা করিতে
    পারেন।
    </span>
</p>

<p class="pl-4 d-flex pt-1">
    <span class="w-26 d-inline-block">(খ)</span>
    <span class="pl-2 text-justify">গেজেটেড অফিসার/ভারপ্রাপ্ত গেজেটেড অফিসার অথবা অফিস-প্রধানগণ দুই প্রস্থে এই ফরমের সংশ্লিষ্ট ঘরগুলি অনুস্বাক্ষর করিয়া পূরণ
    করিবেন ও তাহার পরবর্তী উৰ্দ্ধতন অফিসার ইহাতে প্রতিস্বাক্ষর করিবেন।</span>
</p>
<p class="pl-4 d-flex pt-1">
    <span class="w-26 d-inline-block">(গ)</span>
    <span class="pl-2 text-justify">এই প্রতিবেদন গােপনীয়। তবে ইহাতে কোন বিরূপ মন্তব্য থাকিলে সংশ্লিষ্ট অফিসার/কর্মচারীকে উহা জানাইতে হইবে। প্রতিবেদনাধীন
অফিসার/কর্মচারী ইহার জবাবে বিরূপ মন্তব্য বাতিলের জন্য উপযুক্ত কর্তৃপক্ষের নিকট আবেদন পেশ করিতে পারেন।</span>
</p>

<h6 class="pt-4 pb-2 mb-0 "><b>প্রতিবেদনকারী অফিসারের জন্য নির্দেশঃ</b></h6>
<p class="pl-4 d-flex">
    <span class="w-26 d-inline-block">২। </span>
    <span class="pl-2 text-justify"><span class="pr-1">(ক)</span>
